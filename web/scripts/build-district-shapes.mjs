// Turns a Tamil Nadu district GeoJSON into ready-to-render SVG paths.
//
// Run once, commit the output. Doing the projection at build time rather than
// in the browser means the app ships ~100KB of path strings instead of 400KB of
// coordinates plus a projection on every render.
//
//   node scripts/build-district-shapes.mjs <input.geojson>
//
// Source used: https://raw.githubusercontent.com/udit-001/india-maps-data/main/
//              geojson/states/tamil-nadu.geojson  (37 districts, post-2020)
// The GeoJSON itself is not committed — it is 400KB and this generator's
// output is the only part the app needs.
//
// Source must expose a `district` property per feature. Equirectangular
// projection with a cos(lat) correction is accurate enough for one state — the
// alternative (a real projection library) buys nothing at this scale.

import fs from 'node:fs';
import path from 'node:path';

const input = process.argv[2];
if (!input) {
  console.error('usage: node scripts/build-district-shapes.mjs <input.geojson>');
  process.exit(1);
}

const OUT = path.join('src', 'components', 'dashboard', 'districtMaps', 'tnDistrictShapes.ts');
const VIEW = 1000;      // viewBox width; height derives from the aspect ratio
const PRECISION = 1;    // decimal places kept per coordinate

const geo = JSON.parse(fs.readFileSync(input, 'utf8'));
const features = geo.features.filter((f) => f.geometry);

// --- bounds -----------------------------------------------------------------
let minLng = Infinity, maxLng = -Infinity, minLat = Infinity, maxLat = -Infinity;
const eachRing = (geometry, fn) => {
  const polys = geometry.type === 'Polygon' ? [geometry.coordinates] : geometry.coordinates;
  for (const poly of polys) for (const ring of poly) fn(ring);
};
for (const f of features) {
  eachRing(f.geometry, (ring) => {
    for (const [lng, lat] of ring) {
      if (lng < minLng) minLng = lng; if (lng > maxLng) maxLng = lng;
      if (lat < minLat) minLat = lat; if (lat > maxLat) maxLat = lat;
    }
  });
}

// Longitude degrees shrink with latitude; without this the state comes out
// noticeably too wide.
const midLat = (minLat + maxLat) / 2;
const lngScale = Math.cos((midLat * Math.PI) / 180);
const spanX = (maxLng - minLng) * lngScale;
const spanY = maxLat - minLat;
const scale = VIEW / spanX;
const height = Math.round(spanY * scale);

const project = ([lng, lat]) => [
  +(((lng - minLng) * lngScale) * scale).toFixed(PRECISION),
  // SVG y grows downward; latitude grows upward.
  +(((maxLat - lat) * scale)).toFixed(PRECISION),
];

// --- path building ----------------------------------------------------------
function ringToPath(ring) {
  const pts = [];
  let prev = null;
  for (const coord of ring) {
    const p = project(coord);
    // Drop points that collapse onto the previous one after rounding.
    if (prev && p[0] === prev[0] && p[1] === prev[1]) continue;
    pts.push(p);
    prev = p;
  }
  if (pts.length < 3) return '';
  return `M${pts.map((p) => `${p[0]} ${p[1]}`).join('L')}Z`;
}

const shapes = features.map((f) => {
  let d = '';
  eachRing(f.geometry, (ring) => { d += ringToPath(ring); });
  return { name: f.properties.district, d };
}).filter((s) => s.d).sort((a, b) => a.name.localeCompare(b.name));

const body = shapes.map((s) => `  { name: ${JSON.stringify(s.name)}, d: ${JSON.stringify(s.d)} },`).join('\n');

const out = `// GENERATED by scripts/build-district-shapes.mjs — do not edit by hand.
//
// Tamil Nadu district outlines projected into a ${VIEW}x${height} viewBox.
// ${shapes.length} districts. Regenerate with:
//   node scripts/build-district-shapes.mjs <input.geojson>

export interface DistrictShape {
  /** District name exactly as the source dataset spells it. */
  name: string;
  /** SVG path data in TN_MAP_VIEWBOX coordinates. */
  d: string;
}

export const TN_MAP_VIEWBOX = '0 0 ${VIEW} ${height}';

export const TN_DISTRICT_SHAPES: DistrictShape[] = [
${body}
];
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, out);
console.log(`wrote ${OUT}`);
console.log(`  districts: ${shapes.length}`);
console.log(`  viewBox  : 0 0 ${VIEW} ${height}`);
console.log(`  size     : ${(out.length / 1024).toFixed(0)}KB`);
